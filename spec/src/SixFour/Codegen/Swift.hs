-- | Generate @CellContract.swift@ — the byte-exact NO-BLEND golden for the
-- base/fiber cell algebra ('SixFour.Spec.CellFiber' + '.CellGrid'). Emits the two
-- reserved colours and a fixture battery (each cell's claims in canonical ascending
-- order, its 'CF.render' output, its 'CF.isContested' flag, and the 'CF.shimmerAt'
-- sequence over ticks 0..3). The Swift port must reproduce every value exactly —
-- proving the device honours @lawNoSynthesis@ ("I do not blend") bit-for-bit.
emitCellContract :: Text
emitCellContract = T.unlines $
  [ "// GENERATED by sixfour-spec / Codegen.Swift — do not edit by hand."
  , "// Source of truth: ~/SixFour/spec/src/SixFour/Spec/CellFiber.hs + CellGrid.hs"
  , "// Regenerate with: cabal run spec-codegen"
  , "//"
  , "// NO-BLEND cell contract. render NEVER synthesises a colour:"
  , "//   empty cell      -> neutralColor"
  , "//   single claim    -> that claim, verbatim"
  , "//   >=2 claims      -> contestedSentinel (a loud marker; the collision is SEEN)"
  , "// The opt-in shimmer effect shows one REAL claimant per 20fps tick, indexing"
  , "// `claims` in ASCENDING (L,a,b) order (the canonical, arrival-free fold order)."
  , "// Mirrors Spec.CellFiber lawNoSynthesis / lawShimmerIsClaimant — the Swift"
  , "// renderer must match `golden` byte-for-byte."
  , ""
  , "import simd"
  , ""
  , "public enum CellContract {"
  , "    /// Q16 fixed-point unit (1.0 == q16One). All colour channels are Q16 Int32."
  , "    public static let q16One: Int32 = " <> tInt (fromIntegral q16One)
  , "    /// render(empty): mid-grey L=1/2, a=b=0 — \"clear / unpainted\"."
  , "    public static let neutralColor: SIMD3<Int32> = " <> simd3 CF.neutralColor
  , "    /// render(contested): the reserved loud marker (vivid magenta)."
  , "    public static let contestedSentinel: SIMD3<Int32> = " <> simd3 CF.contestedSentinel
  , ""
  , "    public struct Golden: Sendable {"
  , "        public let name: String"
  , "        public let claims: [SIMD3<Int32>]   // ascending (L,a,b): the canonical order"
  , "        public let render: SIMD3<Int32>     // the no-blend observation"
  , "        public let contested: Bool          // isContested (>=2 claims)"
  , "        public let shimmer: [SIMD3<Int32>]  // shimmerAt t, t in 0..<4"
  , "    }"
  , ""
  , "    /// The fixture battery (computed by SixFour.Spec.CellFiber)."
  , "    public static let golden: [Golden] = ["
  ]
  ++ map cellGolden cellFixtures ++
  [ "    ]"
  , "}"
  ]
  where
    tInt :: Int -> Text
    tInt = T.pack . show

    -- a Q16 colour as a Swift SIMD3<Int32> literal
    simd3 :: CF.Color -> Text
    simd3 (CF.Color (l, a, b)) =
      "SIMD3<Int32>(" <> tInt l <> ", " <> tInt a <> ", " <> tInt b <> ")"

    simd3List :: [CF.Color] -> Text
    simd3List cs = "[" <> T.intercalate ", " (map simd3 cs) <> "]"

    -- Fixed, in-gamut fixtures spanning every render case (empty / clean / contested).
    cellFixtures :: [(String, CF.Cell)]
    cellFixtures =
      let c1 = CF.Color (10000,  2000, -3000)
          c2 = CF.Color (50000, -1000,  4000)
          c3 = CF.Color (30000,  5000,  5000)
      in [ ("empty",     CF.bottom)
         , ("clean",     CF.singletonCell c1)
         , ("contested2", Set.fromList [c1, c2])
         , ("contested3", Set.fromList [c1, c2, c3])
         ]

    cellGolden :: (String, CF.Cell) -> Text
    cellGolden (name, cell) =
      let cs  = CF.claims cell                              -- ascending order
          r   = CF.render cell
          ctd = CF.isContested cell
          sh  = [ CF.shimmerAt t cell | t <- [0 .. 3] ]
      in T.concat
         [ "        Golden(name: \"", T.pack name, "\""
         , ", claims: ", simd3List cs
         , ", render: ", simd3 r
         , ", contested: ", if ctd then "true" else "false"
         , ", shimmer: ", simd3List sh
         , "),"
         ]
