  , "/// O(T·H·W) scan; the brand cannot be forged."
  , "public struct CompleteVoxelVolume: Sendable {"
  , "    /// `frames[f]`: row-major UInt8 indices for frame f, length H·W."
  , "    public let frames: [[UInt8]]"
  , "    public init?(checkingFrames fs: [[UInt8]]) {"
  , "        guard fs.count == SixFourShape.T else { return nil }"
  , "        let perFrame = SixFourShape.pixelsPerFrame"
  , "        for f in fs {"
  , "            guard f.count == perFrame else { return nil }"
  , "            var seen = Set<UInt8>(); seen.reserveCapacity(SixFourShape.K)"
  , "            for x in f { seen.insert(x) }"
  , "            guard seen.count == SixFourShape.K else { return nil }"
  , "        }"
  , "        self.frames = fs"
  , "    }"
  , "}"
  , ""
  , "/// Stage A contract: per-frame quantizer."
  , "public protocol StageAContract {"
  , "    /// Input: H*W OKLab triples (row-major)."
  , "    /// Output: (palette of K OKLab triples, indices of length H*W)."
  , "    func quantize(frameOKLab: [SIMD3<Float>])"
  , "      -> (palette: [SIMD3<Float>], indices: [UInt8])"
  , "}"
  ]

emitMatrix
  :: Text
  -> ((Double, Double, Double), (Double, Double, Double), (Double, Double, Double))
  -> Text
emitMatrix nm ((a, b, c), (d, e, f), (g, h, i)) = T.unlines
  [ "    /// 3x3 OKLab matrix `" <> nm <> "`, row-major."
  , "    public static let " <> nm <> ": [[Float]] = ["
  , "        [" <> tshowF a <> ", " <> tshowF b <> ", " <> tshowF c <> "],"
  , "        [" <> tshowF d <> ", " <> tshowF e <> ", " <> tshowF f <> "],"
  , "        [" <> tshowF g <> ", " <> tshowF h <> ", " <> tshowF i <> "]"
  , "    ]"
  ]

-- | Generate @SignificanceContract.swift@ — the per-frame palette
-- significance constants, the @SixFourSignificantCell@ (a palette slot as a
-- centroid + per-axis range + population + provenance), and the
-- @SignificantVoxelVolume@ brand: a 'CompleteVoxelVolume' in which every slot
-- of every frame is backed by @≥ minPopulation@ pixels (no donated outlier),
-- with exact mass conservation. Bit-mirrors "SixFour.Spec.Significance".
emitSignificanceContract :: Text
emitSignificanceContract = T.unlines
  [ "// GENERATED by sixfour-spec / Codegen.Swift — do not edit by hand."
  , "// Source of truth: ~/SixFour/spec/src/SixFour/Spec/Significance.hs"
  , "// Regenerate with: cabal run spec-codegen"
  , ""
  , "import Foundation"
  , "import simd"
  , ""
  , "/// Per-frame palette significance constants, mirrored from the Haskell"
  , "/// spec so the app, the oracle, and the trainer score significance"
  , "/// identically. Significance is **population**: a palette slot is"
  , "/// significant iff backed by `minPopulation` pixels — the opposite of a"
  , "/// donated single-pixel outlier. Each slot owns the OKLab range"
  , "/// `mean ± confidenceZ · σ` (the \"range for each bin\")."
  , "public enum SixFourSignificance {"
  , "    /// Two-sided 95% normal quantile z_{0.975} for the per-axis range."
  , "    public static let confidenceZ: Double = " <> tshowF confidenceZ
  , "    /// Minimum pixel population for a slot to be significant (anti-outlier)."
  , "    public static let minPopulation: Int = " <> tshow minPopulation
  , "    /// χ²₃ right-tail critical values P(χ²₃ > v) = α. Single source of"
  , "    /// truth — `ClusterStatisticsOps.ChiSquare3` must defer to this."
  , "    public static func chiSquare3Critical(alpha: Double) -> Double {"
  , "        switch alpha {"
  , "        case 0.001: return " <> tshowF (chiSquare3Critical 0.001)
  , "        case 0.01:  return " <> tshowF (chiSquare3Critical 0.01)
  , "        case 0.025: return " <> tshowF (chiSquare3Critical 0.025)
  , "        case 0.05:  return " <> tshowF (chiSquare3Critical 0.05)
  , "        case 0.10:  return " <> tshowF (chiSquare3Critical 0.10)
  , "        default:    return " <> tshowF (chiSquare3Critical 0.05)
  , "        }"
  , "    }"
  , "    /// A frame of `pixels` can be partitioned into `k` all-significant"
  , "    /// slots iff `pixels ≥ minPopulation · k`. The SixFour shape"
  , "    /// (4096 ≥ 2·256 = 512) satisfies this with a factor of 8 to spare,"
  , "    /// so the all-significant guarantee cannot fail."
  , "    public static func feasible(pixels: Int, k: Int) -> Bool {"
  , "        pixels >= minPopulation * k"
  , "    }"
  , "}"
  , ""
  , "/// Where a palette slot's population came from."
  , "public enum SixFourProvenance: String, Sendable, Codable, CaseIterable {"
  , "    case extracted   // a genuine cluster mode (count ≥ minPopulation)"
  , "    case split       // a sub-mode created to reach K (count ≥ minPopulation)"
  , "    case degenerate  // could not be backed by minPopulation pixels — unreachable on the SixFour shape"
  , "}"
  , ""
  , "/// One palette slot as a statistical cell: centroid, per-axis std-dev"
  , "/// (√ of the OKLab covariance diagonal), population, and provenance."
  , "/// The OKLab range it owns is `mean ± confidenceZ · stdDev`."
  , "public struct SixFourSignificantCell: Sendable {"
  , "    public let mean: SIMD3<Float>"
  , "    public let stdDev: SIMD3<Float>"
  , "    public let count: Int"
  , "    public let provenance: SixFourProvenance"
  , ""
  , "    public init(mean: SIMD3<Float>, stdDev: SIMD3<Float>, count: Int, provenance: SixFourProvenance) {"
  , "        self.mean = mean; self.stdDev = stdDev; self.count = count; self.provenance = provenance"
  , "    }"
  , ""
  , "    /// Lower corner of the α-confidence range box."
  , "    public var rangeLo: SIMD3<Float> { mean - Float(SixFourSignificance.confidenceZ) * stdDev }"
  , "    /// Upper corner of the α-confidence range box."
  , "    public var rangeHi: SIMD3<Float> { mean + Float(SixFourSignificance.confidenceZ) * stdDev }"
  , "    /// Significant ⇔ backed by ≥ minPopulation pixels (not an outlier)."
  , "    public var isSignificant: Bool { count >= SixFourSignificance.minPopulation }"
  , "}"
  , ""
  , "/// `SignificantVoxelVolume` brand: a `CompleteVoxelVolume` (every frame"
  , "/// surjective onto all K slots) in which, additionally, every palette"
  , "/// slot of every frame is statistically significant — backed by"
  , "/// `≥ minPopulation` pixels (so none is a donated outlier) — with exact"
  , "/// per-frame mass conservation. The per-frame `cells` carry the OKLab"
  , "/// ranges. Constructed only by `init?`, which performs the O(T·K) scan;"
  , "/// the brand cannot be forged. The GIF encoder consumes `.complete`."
  , "///"
  , "/// Laws (cross-reference SixFour.Spec.Significance):"
  , "///   * Def 22 MassConservation — Σ count = pixelsPerFrame, per frame."
  , "///   * Def 23 AllSignificant   — every slot count ≥ minPopulation."
  , "///   * Thm 7 NoOutlier         — significance is population, never a count-1 slot."
  , "public struct SignificantVoxelVolume: Sendable {"
  , "    public let complete: CompleteVoxelVolume"
  , "    /// `cells[f]`: the K palette cells of frame f, in slot order."
  , "    public let cells: [[SixFourSignificantCell]]"
  , ""
  , "    public init?(complete: CompleteVoxelVolume, cells: [[SixFourSignificantCell]]) {"
  , "        guard cells.count == SixFourShape.T else { return nil }"
  , "        for frame in cells {"
  , "            guard frame.count == SixFourShape.K else { return nil }"
  , "            var mass = 0"
  , "            for c in frame {"
  , "                guard c.isSignificant else { return nil }   // no outlier slots"
  , "                mass += c.count"
  , "            }"
  , "            guard mass == SixFourShape.pixelsPerFrame else { return nil }"
  , "        }"
  , "        self.complete = complete"
  , "        self.cells = cells"
  , "    }"
  , ""
  , "    /// The wire-relevant per-frame index frames (delegated to the brand"
  , "    /// the encoder already accepts)."
  , "    public var frames: [[UInt8]] { complete.frames }"
  , "}"
  ]

