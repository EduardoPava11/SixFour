  , "        for r in 0..<" <> tInt L.rows <> " { for c in 0..<" <> tInt L.cols <> " {"
  , "            counts[Int(ownerAt(col: c, row: r).rawValue)] += 1"
  , "        } }"
  , "        guard counts == [" <> T.intercalate ", " (map (tInt . ownerCount) Own.allOwners)
        <> "] else { return false }"
  , "        // Fusion: the live-face shutter∩palette co-occupancy is a declared EffectZone."
  , "        guard isFused(.shutter, .palette) else { return false }"
  , "        // Refresh + decode: 20 fps, and a sample cell's colour decodes to its owner."
  , "        guard refreshHz == 20, ownerColorInv(fieldColorAt(col: 50, row: 50)) == .preview else { return false }"
  , "        return true"
  , "    }"
  , "}"
  ]
  where
    tInt :: Int -> Text
    tInt = T.pack . show

    simd3 :: CF.Color -> Text
    simd3 (CF.Color (l, a, b)) =
      "SIMD3<Int32>(" <> tInt l <> ", " <> tInt a <> ", " <> tInt b <> ")"

    -- Swift enum case name = the lowercased owner constructor (Preview -> preview).
    caseName :: Own.Owner -> Text
    caseName = T.toLower . T.pack . show

    badge :: Own.Owner -> CF.Color
    badge = Own.unOwnerColor . Own.ownerColor

    ownerCase :: Own.Owner -> Text
    ownerCase o = "    case " <> caseName o <> " = " <> tInt (fromEnum o)

    ownerColorCase :: Own.Owner -> Text
    ownerColorCase o = "        case ." <> caseName o <> ": return " <> simd3 (badge o)

    ownerTableRow :: Own.Owner -> Text
    ownerTableRow o = "        (." <> caseName o <> ", " <> simd3 (badge o) <> "),"

    -- A Governs value as its Swift SFGoverns literal.
    governsSwift :: Own.Governs -> Text
    governsSwift g = case g of
      Own.GovernsFrames n  -> ".frames("  <> tInt n <> ")"
      Own.GovernsPalette n -> ".palette(" <> tInt n <> ")"
      Own.GovernsControl   -> ".control"
      Own.GovernsGauge n   -> ".gauge("   <> tInt n <> ")"
      Own.GovernsGround    -> ".ground"
      Own.GovernsTitle     -> ".title"

    respSwift :: Own.Responsibility -> Text
    respSwift r = "SFResponsibility(governs: " <> governsSwift (Own.reGoverns r)
               <> ", cadenceHz: " <> tInt (Own.reCadenceHz r) <> ")"

    respCase :: Own.Owner -> Text
    respCase o = "        case ." <> caseName o <> ": return " <> respSwift (Own.responsibility o)

    respTableRow :: Own.Owner -> Text
    respTableRow o = "        (." <> caseName o <> ", " <> respSwift (Own.responsibility o) <> "),"

    regionRow :: Own.OwnerRegion -> Text
    regionRow r = T.concat
      [ "        SFOwnerRegion(owner: .", caseName (Own.orOwner r)
      , ", col: ", tInt (Own.orCol r), ", row: ", tInt (Own.orRow r)
      , ", w: ",   tInt (Own.orW r),   ", h: ",   tInt (Own.orH r)
      , ", interactive: ", if Own.orInteractive r then "true" else "false", "),"
      ]

    coverRow :: (Own.Atom, Own.Owner) -> Text
    coverRow (a, o) = T.concat
      [ "        (", tInt (Own.atomCol a), ", ", tInt (Own.atomRow a)
      , ", .", caseName o, "),"
      ]

    fusionRow :: (Own.Owner, Own.Owner) -> Text
    fusionRow (a, b) = "        (." <> caseName a <> ", ." <> caseName b <> "),"

    -- The number of atoms ownerAt assigns to an owner over the whole scene (computed
    -- once at codegen time), emitted as the selfCheck expected tally.
    sceneOwners :: [Own.Owner]
    sceneOwners = map (Own.ownerAt Own.captureScene) Own.allAtoms

    ownerCount :: Own.Owner -> Int
    ownerCount o = length (filter (== o) sceneOwners)
