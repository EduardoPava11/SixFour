{- |
Module      : SixFour.Codegen.ModelIO
Description : Emit @SixFourModelIO.swift@ — the wireable model I/O boundary the iOS app paints into and renders out of. Mirrors "SixFour.Spec.ModelIO" (the boundary types + floor) and "SixFour.Spec.ModelForward" (the nudge-conditioned frame), pinning the control-grid dims, the 9 paint channels, the φ6 gauge, the A_7 detail-band count, and the byte-exact floor margin so the hand-written Swift paint tool (W2.2) and render surface (W2.1) defer to spec constants.

String-templated like the rest of "SixFour.Codegen.Swift" (read, diffed, reviewed by humans). The
generated constants come from the spec values, so the hermetic-codegen gate step diffs this file against
