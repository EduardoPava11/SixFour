-- | Generate @GlobalVolumeContract.swift@ — the whole-GIF brands for a single
-- global palette (GIFB). Mirrors 'SixFour.Spec.GlobalVolume'. The Swift logic
-- references 'SixFourShape' / 'SixFourSignificance' at runtime, so this emitter is
-- a fixed template (no Haskell-constant interpolation).
emitGlobalVolumeContract :: Text
emitGlobalVolumeContract = T.unlines
  [ "// GENERATED by sixfour-spec / Codegen.Swift — do not edit by hand."
  , "// Source of truth: ~/SixFour/spec/src/SixFour/Spec/GlobalVolume.hs"
  , "// Regenerate with: cabal run spec-codegen"
  , ""
  , "import Foundation"
  , ""
  , "/// `GlobalCompleteVolume` brand: a single-global-palette (GIFB) index volume is"
  , "/// complete iff exactly `SixFourShape.T` frames, each `pixelsPerFrame` long, and"
  , "/// the UNION over all frames is surjective onto all `SixFourShape.K` slots. A"
  , "/// single frame need NOT use all K — the per-frame `CompleteVoxelVolume`"
  , "/// requirement is RELAXED to the whole GIF (a global palette is shared, so one"
  , "/// frame may touch only some colours). Mirrors `SixFour.Spec.GlobalVolume`."
  , "/// Constructed only by `init?(checkingFrames:)`; the brand cannot be forged."
  , "public struct GlobalCompleteVolume: Sendable {"
