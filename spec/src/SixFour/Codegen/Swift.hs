{- |
Module      : SixFour.Codegen.Swift
Description : Emit Swift contracts to @SixFour/Generated/@.

Produces two files:

  * @StageContract.swift@ — the Stage A quantizer contract and the
    @CompleteVoxelVolume@ brand type the iOS app uses to assert
    strict per-frame surjectivity at encode time.
  * @NetContract.swift@   — slot enum + input/output dimensions for the
    deferred NN slots (metric / merger / embedder).

The emitter is intentionally **string-templated** rather than using
any pretty-printer library: the generated code is read by humans,
diffed by humans, and reviewed by humans, so legibility wins over
combinator elegance.
-}
module SixFour.Codegen.Swift
  ( emitStageContract
  , emitNetContract
  , emitSTBN3DContract
  , emitSignificanceContract
  , emitGlobalVolumeContract
  ) where

import qualified Data.Text    as T
import           Data.Text    (Text)

import SixFour.Spec.Color    (m1, m2)
import SixFour.Spec.Shape
import SixFour.Spec.Coverage (coverageBinsPerAxis)
import SixFour.Spec.Significance (confidenceZ, minPopulation, chiSquare3Critical)
import SixFour.Spec.Net

-- | Generate @GlobalVolumeContract.swift@ — the whole-GIF brands for a single
-- global palette (GIFB). Mirrors 'SixFour.Spec.GlobalVolume'. The Swift logic
-- references 'SixFourShape' / 'SixFourSignificance' at runtime, so this emitter is
-- a fixed template (no Haskell-constant interpolation).
emitGlobalVolumeContract :: Text
emitGlobalVolumeContract = T.unlines
  [ "// GENERATED by sixfour-spec / Codegen.Swift — do not edit by hand."
  , "// Source of truth: ~/SixFour/spec/src/SixFour/Spec/GlobalVolume.hs"
  , "// Regenerate with: cabal run spec-codegen"
  , ""
  , "import Foundation"
  , ""
  , "/// `GlobalCompleteVolume` brand: a single-global-palette (GIFB) index volume is"
  , "/// complete iff exactly `SixFourShape.T` frames, each `pixelsPerFrame` long, and"
  , "/// the UNION over all frames is surjective onto all `SixFourShape.K` slots. A"
  , "/// single frame need NOT use all K — the per-frame `CompleteVoxelVolume`"
  , "/// requirement is RELAXED to the whole GIF (a global palette is shared, so one"
  , "/// frame may touch only some colours). Mirrors `SixFour.Spec.GlobalVolume`."
  , "/// Constructed only by `init?(checkingFrames:)`; the brand cannot be forged."
  , "public struct GlobalCompleteVolume: Sendable {"
  , "    public let frames: [[UInt8]]"
  , "    public init?(checkingFrames fs: [[UInt8]]) {"
  , "        guard fs.count == SixFourShape.T else { return nil }"
  , "        let perFrame = SixFourShape.pixelsPerFrame"
  , "        var seen = Set<UInt8>(); seen.reserveCapacity(SixFourShape.K)"
  , "        for f in fs {"
  , "            guard f.count == perFrame else { return nil }"
  , "            for x in f { seen.insert(x) }"
  , "        }"
  , "        guard seen.count == SixFourShape.K else { return nil }"
  , "        self.frames = fs"
  , "    }"
  , "}"
  , ""
  , "/// `GlobalSignificantVolume` brand: every global slot is backed by"
  , "/// `≥ SixFourSignificance.minPopulation` POOLED pixels (summed over the whole"
  , "/// GIF, not per frame), and the total mass is exactly `T · pixelsPerFrame`. So"
  , "/// \"no empty / donated-outlier slot ships\" survives at GIF scale (guaranteed by"
  , "/// the whole-GIF significance rescue). Mirrors `SixFour.Spec.GlobalVolume.globalSignificant`."
  , "public struct GlobalSignificantVolume: Sendable {"
  , "    public let complete: GlobalCompleteVolume"
  , "    /// `pooledCounts[j]` = total pixels assigned to global slot j across all frames."
  , "    public init?(complete: GlobalCompleteVolume, pooledCounts: [Int]) {"
  , "        guard pooledCounts.count == SixFourShape.K else { return nil }"
  , "        guard pooledCounts.reduce(0, +) == SixFourShape.T * SixFourShape.pixelsPerFrame else { return nil }"
  , "        for c in pooledCounts where c < SixFourSignificance.minPopulation { return nil }"
  , "        self.complete = complete"
  , "    }"
  , "}"
  ]

-- | Generate @StageContract.swift@.
emitStageContract :: Text
emitStageContract = T.unlines
  [ "// GENERATED by sixfour-spec / Codegen.Swift — do not edit by hand."
  , "// Source of truth: ~/SixFour/spec/src/SixFour/Spec/Shape.hs +"
  , "//                  ~/SixFour/spec/src/SixFour/Spec/Color.hs"
  , "// Regenerate with: cabal run spec-codegen"
  , ""
  , "import Foundation"
  , "import simd"
  , ""
  , "/// Compile-time SixFour shape constants. These four numbers are"
  , "/// the only place these magic numbers may appear; anywhere else"
  , "/// in the iOS app must reference them through `SixFourShape`."
  , "public enum SixFourShape {"
  , "    public static let T: Int = " <> tshow tVal
  , "    public static let H: Int = " <> tshow hVal
  , "    public static let W: Int = " <> tshow wVal
  , "    public static let K: Int = " <> tshow kVal
  , "    public static let pixelsPerFrame: Int = " <> tshow pixelsPerFrame
  , "    public static let pixelsPerGIF: Int   = " <> tshow pixelsPerGIF
  , "    public static let candidatesAfterStageA: Int = " <> tshow candidatesAfterStageA
  , "    /// Bins per OKLab axis for the gamut-coverage diversity metric"
  , "    /// (`coverageBinsPerAxis³` voxels). Pinned to SixFour.Spec.Coverage."
  , "    public static let coverageBinsPerAxis: Int = " <> tshow coverageBinsPerAxis
  , "}"
  , ""
  , "/// OKLab triple, bit-exact mirror of `SixFour/Color/ColorScience.swift`."
  , "/// Constants below MUST match — `cabal test` checks this every CI run."
  , "public enum SixFourOKLabConstants {"
  , emitMatrix "M1" m1
  , emitMatrix "M2" m2
  , "}"
  , ""
  , "/// `CompleteVoxelVolume` brand: proof that a per-frame index volume is"
  , "/// a complete T×H×W voxel cube — exactly `SixFourShape.T` frames, each"
  , "/// of `SixFourShape.pixelsPerFrame` indices, and each frame"
  , "/// independently surjective onto all `SixFourShape.K` palette slots"
  , "/// (strict per-frame surjectivity). The GIF encoder accepts only this"
  , "/// type, so a GIF with a dropped frame, a short frame, or a frame that"
  , "/// fails to exercise all K colours is unrepresentable at the call site."
