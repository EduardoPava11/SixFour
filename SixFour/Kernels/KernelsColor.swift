private let colorSrgbLinearLut: [Int32] =
    s4I32TableLE(s4LutSrgbLinear, expectedBytes: 1024, name: "srgb_linear_lut.bin")

// The transcendentals (Log3G10 decode, filmic exp) + the Q16 sRGB-encode output
