/// Q16 unit as the i64 the arithmetic runs in (kernels.zig `Q16_ONE` widened).
private let q16One64: Int64 = 1 << 16

// ── Embedded LUT reinterpretation (the @embedFile / bytesAsSlice twin) ───────

/// Compose a little-endian i32 table from raw golden bytes — the exact
/// semantics of Zig `std.mem.readInt(i32, lut[i*4..][0..4], .little)`.
private func s4I32TableLE(_ bytes: [UInt8], expectedBytes: Int, name: StaticString) -> [Int32] {
    precondition(bytes.count == expectedBytes, "\(name): embedded LUT byte-count mismatch")
    var out = [Int32]()
    out.reserveCapacity(bytes.count / 4)
    var i = 0
    while i < bytes.count {
        let u = UInt32(bytes[i])
            | (UInt32(bytes[i + 1]) << 8)
            | (UInt32(bytes[i + 2]) << 16)
            | (UInt32(bytes[i + 3]) << 24)
        out.append(Int32(bitPattern: u))
        i += 4
    }
    return out
}

/// The shared inverse-gamma LUT, generated by `spec-fixtures` from the Double
/// linearToSRGB and embedded byte-for-byte. Index = clamped Q16 linear ∈ [0,65536];
/// value = round(255·sRGB). Deterministic on every device — no runtime pow.
/// (Zig: GAMMA_LUT = @embedFile("gamma_lut.bin"), 65537 raw bytes — used as u8
/// directly, no reinterpretation.)
private let colorGammaLut: [UInt8] = {
    precondition(s4LutGamma.count == 65537, "gamma_lut.bin must be 65537 bytes; run `cd spec && cabal run spec-fixtures`")
    return s4LutGamma
}()

/// Embedded sRGB→linear LUT: 256 little-endian i32 of round(srgbToLinear(b/255)·2^16),
/// emitted by spec/app/Fixtures.hs from the SAME SixFour.Spec.Color.srgbToLinear, so
