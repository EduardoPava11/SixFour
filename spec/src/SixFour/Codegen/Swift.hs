  , "public enum SixFourNetIO {"
  , "    public struct Spec: Sendable {"
  , "        public let inputDim: Int"
  , "        public let outputDim: Int"
  , "        public let description: String"
  , "    }"
  , emitSlotSpec "metric" slotMetricDims
  , ""
  , emitSlotSpec "look" slotLookDims
  , ""
  , "    /// The metric organ ships as the 6 upper-triangle floats of the"
  , "    /// 3x3 PSD matrix M = L Lᵀ produced by trainer/train_metric.py."
  , "    public static let metricPSDUpperTriangleCount: Int = "
      <> tshow metricPSDUpperTriangleCount
  , ""
  , "    /// The look-NN FORM (σ-pair decoder), the single canonical seam the MLX"
  , "    /// trainer, the Haskell golden, and the on-device forward pass all share."
  , "    /// `lookSigmaPairDOF` = the 384 SigmaPairTree coefficients the decoder emits"
  , "    /// (= 3·128 σ-pair generators), reconstructed into the 256-leaf palette."
  , "    /// NOT 768: 768 is the FLAT leaf space (256·3) the genome reconstructs INTO."
  , "    public static let lookSigmaPairDOF: Int = " <> tshow (netOutputDim slotLookDims)
  , "    /// MODEL_DIM / CORE_DEPTH / SIGMA_PAIR_LEAVES / MAX_TOKENS (mirrors"
  , "    /// net_shape.py aux_dims; source SixFour.Spec.Net.slotLookDims)."
  , "    public static let lookAuxDims: [String: Int] = "
      <> auxDictLiteral (netAuxDims slotLookDims)
  , "}"
  ]

emitSlotSpec :: Text -> NetIO -> Text
emitSlotSpec nm io = T.unlines
  [ "    public static let " <> nm <> ": Spec = Spec("
  , "        inputDim: "    <> tshow (netInputDim io) <> ","
  , "        outputDim: "   <> tshow (netOutputDim io) <> ","
  , "        description: \"" <> T.pack (netDescription io) <> "\""
  , "    )"
  ]

-- | A Swift `[String: Int]` literal mirroring the slot's aux dims. The source
-- list (Net.slotLookDims) has a fixed order, so the generated text is deterministic.
auxDictLiteral :: [(String, Int)] -> Text
auxDictLiteral xs =
  "[" <> T.intercalate ", "
         [ "\"" <> T.pack k <> "\": " <> tshow v | (k, v) <- xs ]
     <> "]"

tshow :: Show a => a -> Text
tshow = T.pack . show

-- | Emit a Double as a Swift Float literal with full precision.
tshowF :: Double -> Text
tshowF x = T.pack (show x)

-- | Generate @STBN3DContract.swift@. Emits ONLY:
--
--   * 'STBN3DMaskLoader' — reads @Resources/stbn3d-8.bin@ (the 8³ blue-noise
--     tile produced by 'SixFour.Spec.STBN3D.generateSTBN3D') and tiles it to
--     the full 64³ mask consumed by 'PaletteGenerator.swift's blue-noise
--     dither path.
--
-- The retired @emitHybridContract@ also emitted 'SixFourHybridShape',
-- 'SixFourHybridParams', 'SixFourHybridSlot', and three witness types for the
-- trunk+delta palette direction; that direction was deleted when the math-first
-- LookNetE/R/D pipeline replaced it. The STBN3D mask survives because it is
-- the on-device blue-noise reference; nothing else in the Hybrid namespace did.
emitSTBN3DContract :: Text
emitSTBN3DContract = T.unlines
  [ "// GENERATED by sixfour-spec / Codegen.Swift — do not edit by hand."
  , "// Source of truth: ~/SixFour/spec/src/SixFour/Spec/STBN3D.hs"
  , "// Regenerate with: cabal run spec-codegen"
  , ""
  , "import Foundation"
  , ""
  , "/// Loader for the precomputed scalar 3D blue-noise mask."
  , "///"
  , "/// The bit-exact ground truth is `SixFour.Spec.STBN3D`'s"
  , "/// `generateSTBN3D @8 @8 @8`, written to `Resources/stbn3d-8.bin`"
  , "/// (512 bytes). `loadTiled` tiles that 8³ pattern toroidally to"
  , "/// produce the full 64³ mask. The tile uses toroidal distance"
  , "/// during generation, so the tiling is mathematically clean: no"
  , "/// edge discontinuities, only a soft 8-voxel periodic structure."
  , "public enum STBN3DMaskLoader {"
  , "    /// Returns the raw 8³ tile (512 bytes) loaded from the bundle."
  , "    public static func loadTile(bundle: Bundle = .main) -> [UInt8]? {"
  , "        guard let url = bundle.url(forResource: \"stbn3d-8\","
  , "                                    withExtension: \"bin\"),"
  , "              let data = try? Data(contentsOf: url),"
  , "              data.count == 8 * 8 * 8"
  , "        else { return nil }"
  , "        return Array(data)"
  , "    }"
  , ""
  , "    /// Returns the full 64³ mask, produced by tiling the 8³ tile"
  , "    /// 8 × 8 × 8 times along (t, y, x)."
  , "    public static func loadTiled(bundle: Bundle = .main) -> [UInt8]? {"
  , "        guard let tile = loadTile(bundle: bundle) else { return nil }"
  , "        let T = SixFourShape.T"
  , "        let H = SixFourShape.H"
  , "        let W = SixFourShape.W"
  , "        var out = [UInt8](repeating: 0, count: T * H * W)"
  , "        for f in 0..<T { for y in 0..<H { for x in 0..<W {"
  , "            out[(f * H + y) * W + x] = tile[((f % 8) * 8 + (y % 8)) * 8 + (x % 8)]"
  , "        } } }"
  , "        return out"
  , "    }"
  , "}"
  ]
