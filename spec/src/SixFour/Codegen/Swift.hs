  , "    public static func threeDFrontFace(_ cursor: Int, count n: Int) -> Int {"
  , "        frontFaceFrame(cursor, depth: n - 1, count: n)"
  , "    }"
  , ""
  , "    /// One full cycle of advances from frame 0: `[1, 2, ..., N-1, 0]`."
