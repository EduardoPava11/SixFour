-- | Format an @[Int]@ as a Python list literal: @[1, 2, 3]@.
pyIntList :: [Int] -> Text
pyIntList xs = "[" <> T.intercalate ", " (map tshow xs) <> "]"

-- | Format a @[Bool]@ as a Python list literal: @[True, False, ...]@.
pyBoolList :: [Bool] -> Text
pyBoolList xs = "[" <> T.intercalate ", " (map pyB xs) <> "]"
  where
    pyB True  = "True"
    pyB False = "False"

tshow :: Show a => a -> Text
tshow = T.pack . show
