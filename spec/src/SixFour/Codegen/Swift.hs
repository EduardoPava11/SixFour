-- | Generate @CellShapesContract.swift@ — the golden 64-tick ring endpoint table
-- and the ring-tick radius, mirrored from 'SixFour.Spec.CellShapes'. The renderer
-- indexes this table instead of recomputing @atan2@ per cell. @selfCheck()@
-- recomputes the endpoints from @sin@/@cos@ and asserts equality — a live
-- Haskell↔Swift IEEE-754 parity gate (the table is portable because it uses
-- @floor@, not @round@).
emitCellShapesContract :: Text
emitCellShapesContract = T.unlines $
  [ "// GENERATED by sixfour-spec / Codegen.Swift — do not edit by hand."
  , "// Source of truth: ~/SixFour/spec/src/SixFour/Spec/CellShapes.hs"
  , "// Regenerate with: cabal run spec-codegen"
  , ""
  , "import Foundation"
  , "import simd"
  , ""
  , "/// Golden cell-mask geometry for the GRID HUD primitives. The diversity gauge's"
  , "/// 64 tick endpoints are precomputed here (one cell per tick, k=0 at top,"
  , "/// clockwise) so the renderer never recomputes `atan2` per cell. Mirrors"
  , "/// `SixFour.Spec.CellShapes`; the table is bit-portable because it uses `floor`"
  , "/// (identical IEEE-754 on both sides), not `round`."
  , "public enum SixFourCellShapes {"
  , "    /// The radius (in cells, from the sprite centre) at which a gauge tick ends."
  , "    public static let ringTickRadius: Double = " <> tshowF CS.ringTickRadius
  , ""
  , "    /// The 64 tick outer-endpoint cells, in tick order (k = 0 at top, clockwise)."
  , "    /// `x` = column, `y` = row, in the 60×60 gauge sprite."
  , "    public static let ringTickEndpoints: [SIMD2<Int>] = ["
  ]
  ++ [ "        SIMD2<Int>(" <> tshow c <> ", " <> tshow r <> ")," | (c, r) <- CS.ringTickEndpoints ]
  ++
  [ "    ]"
  , ""
  , "    /// Recomputes the endpoints from sin/cos and asserts equality with the table"
  , "    /// above — proving Swift's IEEE-754 floor matches the Haskell golden."
  , "    public static func selfCheck() -> Bool {"
  , "        let c0 = Double(SixFourLattice.ringCells) / 2"
  , "        let n  = SixFourLattice.ringTicks"
  , "        guard ringTickEndpoints.count == n else { return false }"
  , "        for k in 0..<n {"
  , "            let theta = 2 * Double.pi * Double(k) / Double(n)"
  , "            let px = c0 + ringTickRadius * sin(theta)"
  , "            let py = c0 - ringTickRadius * cos(theta)"
  , "            let want = SIMD2<Int>(Int(floor(px)), Int(floor(py)))"
  , "            if ringTickEndpoints[k] != want { return false }"
  , "        }"
  , "        return true"
  , "    }"
  , "}"
  ]

-- | A Swift @[SIMD2<Int>]@ cell-list literal.
cellListLiteral :: [(Int, Int)] -> Text
cellListLiteral cells =
  "[" <> T.intercalate ", " [ "SIMD2<Int>(" <> tshow c <> ", " <> tshow r <> ")" | (c, r) <- cells ] <> "]"

